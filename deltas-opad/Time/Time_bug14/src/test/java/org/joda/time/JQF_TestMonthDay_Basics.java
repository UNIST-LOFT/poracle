    protected void setUp() throws Exception {
        DateTimeUtils.setCurrentMillisFixed(TEST_TIME_NOW);
        zone = DateTimeZone.getDefault();
        DateTimeZone.setDefault(LONDON);
    }

    protected void tearDown() throws Exception {
        DateTimeUtils.setCurrentMillisSystem();
        DateTimeZone.setDefault(zone);
        zone = null;
    }
    @Fuzz
    public void testPlusMonths_int_fromLeap(@InRange(minInt=1, maxInt=12, isFixed=true) int month,
                                            @InRange(minInt=1, maxInt=28, isFixed=true) int day,
                                            @InRange(minInt=1, maxInt=12, isFixed=true) int add
                                            ) {

        try {
            setUp();
            MonthDay test = new MonthDay(month, day, ISOChronology.getInstanceUTC());
            final MonthDay result1 = test.plusMonths(add);
            if (Log.runBuggyVersion) Log.logOutIf(true, () -> new String[]{ "No exception" });
            else Log.ignoreOut();
            tearDown();
        } catch (Exception e){
            Log.logOutIf(true, () -> new String[] {e.getClass().toString()});
        }
    }
